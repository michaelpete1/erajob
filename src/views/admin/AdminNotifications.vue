interface Notification {
  id: string;
  type: 'priority' | 'info' | 'success' | 'default';
  title: string;
  description: string;
  time: string;
  actions: string[];
  read: boolean;
}

interface Filter {
  id: string;
  name: string;
  count: number;
}

const activeFilter = ref<string>('all');
const sortBy = ref<string>('newest');
const notifications = ref<Notification[]>([
